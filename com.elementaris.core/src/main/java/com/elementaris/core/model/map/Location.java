package com.elementaris.core.model.map;

import game.core.model.AbstractPersistentObject;

public class Location extends AbstractPersistentObject {
	private int x;
	private int y;

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

}
